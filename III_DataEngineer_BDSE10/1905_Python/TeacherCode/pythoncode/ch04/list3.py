# list slicing
list1 = list('Hello World!')
print("list1 =",list1)
s = list1[1:3]
print("list1[1:3] =",s)
s = list1[1:5]
print("list1[1:5] =",s)
s = list1[:7]
print("list1[:7] =",s)
s = list1[4:]
print("list1[4:] =",s)
s = list1[1:-1]
print("list1[1:-1] =",s)
s = list1[6:-2]
print("list1[6:-2] =",s)